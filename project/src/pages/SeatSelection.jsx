import React, { useState, useEffect } from 'react';
import GridLayout from 'react-grid-layout';
import 'react-grid-layout/css/styles.css';
import 'react-resizable/css/styles.css';

const SeatSelection = () => {
    const [step, setStep] = useState(1);
    const [layoutData, setLayoutData] = useState(null);
    const [selectedSeats, setSelectedSeats] = useState([]);
    const [formData, setFormData] = useState({
        name: '',
        email: '',
        phone: '',
    });

    useEffect(() => {
        fetch('/room.json')
            .then((res) => res.json())
            .then((data) => setLayoutData(data))
            .catch((err) => console.error('Kh√¥ng th·ªÉ t·∫£i s∆° ƒë·ªì:', err));
    }, []);

    const handleSelectSeat = (id) => {
        setSelectedSeats((prev) =>
            prev.includes(id) ? prev.filter((s) => s !== id) : [...prev, id]
        );
    };

    const handleConfirm = () => {
        alert(`‚úÖ ƒê·∫∑t v√© th√†nh c√¥ng!\n\nH·ªç t√™n: ${formData.name}\nEmail: ${formData.email}\nSƒêT: ${formData.phone}\nGh·∫ø: ${selectedSeats.join(', ')}`);
        setStep(1);
        setSelectedSeats([]);
        setFormData({ name: '', email: '', phone: '' });
    };

    const handleNext = () => {
        if (step === 1 && selectedSeats.length === 0) {
            alert('Vui l√≤ng ch·ªçn √≠t nh·∫•t m·ªôt gh·∫ø!');
            return;
        }
        if (step === 2 && (!formData.name || !formData.email || !formData.phone)) {
            alert('Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß th√¥ng tin!');
            return;
        }
        setStep(step + 1);
    };

    const handleBack = () => {
        if (step > 1) setStep(step - 1);
    };

    if (!layoutData) return <div className="p-10 text-center">ƒêang t·∫£i s∆° ƒë·ªì ph√≤ng...</div>;

    const { elements, cols, backgroundColor } = layoutData;

    return (
        <div className="seat-selection w-full min-h-screen bg-gray-100 flex flex-col items-center px-4 py-10">
            {/* CSS ƒë·ªÉ ·∫©n n√∫t resize ch·ªâ trong trang n√†y */}
            <style>
                {`
                .seat-selection .react-resizable-handle {
                    display: none !important;
                }
                `}
            </style>

            <h2 className="text-2xl md:text-3xl font-bold text-blue-800 mb-6 text-center">
                {step === 1 && 'ü™ë Ch·ªçn gh·∫ø c·ªßa b·∫°n'}
                {step === 2 && 'üìù Nh·∫≠p th√¥ng tin ƒë·∫∑t v√©'}
                {step === 3 && '‚úÖ X√°c nh·∫≠n ƒë·∫∑t v√©'}
            </h2>

            {/* B∆∞·ªõc 1: Ch·ªçn gh·∫ø */}
            {step === 1 && (
                <div
                    className="flex-1 overflow-auto border p-4 rounded-lg shadow bg-white w-full mb-6"
                    style={{ backgroundColor, height: 'calc(100vh - 250px)' }}
                >
                    <GridLayout
                        layout={elements.map((el) => ({
                            ...el,
                            w: el.w || 1,
                            h: el.h || 1,
                        }))}
                        cols={cols}
                        rowHeight={60}
                        width={cols * 60}
                        isDraggable={false}
                        isResizable={false}
                        preventCollision
                        compactType={null}
                    >
                        {elements.map((el) => (
                            <div
                                key={el.i}
                                onClick={() => el.type === 'chair' && handleSelectSeat(el.i)}
                                className={`cursor-pointer flex items-center justify-center font-bold relative ${el.type === 'chair' ? 'hover:scale-105 transition-transform' : ''
                                    }`}
                                style={{
                                    width: '100%',
                                    height: '100%',
                                    backgroundColor: selectedSeats.includes(el.i)
                                        ? '#10b981'
                                        : el.color,
                                    borderRadius: el.shape === 'circle' ? '50%' : '0',
                                    transform: el.shape === 'diamond' ? 'rotate(45deg)' : 'none',
                                    color: el.type === 'chair' ? 'white' : 'black',
                                    fontSize: 14,
                                    overflow: 'hidden',
                                    display: 'flex',
                                    alignItems: 'center',
                                    justifyContent: 'center',
                                }}
                            >
                                <div
                                    style={{
                                        transform: el.shape === 'diamond' ? 'rotate(-45deg)' : 'none',
                                        width: '100%',
                                        textAlign: 'center',
                                        pointerEvents: 'none',
                                    }}
                                >
                                    {el.label}
                                </div>
                            </div>
                        ))}
                    </GridLayout>
                </div>
            )}

            {/* B∆∞·ªõc 2: Nh·∫≠p th√¥ng tin */}
            {step === 2 && (
                <div className="bg-white p-6 rounded-lg shadow w-full max-w-md mb-6">
                    <label className="block mb-2 font-medium">H·ªç t√™n:</label>
                    <input
                        type="text"
                        className="w-full mb-4 p-2 border rounded"
                        value={formData.name}
                        onChange={(e) => setFormData({ ...formData, name: e.target.value })}
                    />
                    <label className="block mb-2 font-medium">Email:</label>
                    <input
                        type="email"
                        className="w-full mb-4 p-2 border rounded"
                        value={formData.email}
                        onChange={(e) => setFormData({ ...formData, email: e.target.value })}
                    />
                    <label className="block mb-2 font-medium">S·ªë ƒëi·ªán tho·∫°i:</label>
                    <input
                        type="text"
                        className="w-full mb-4 p-2 border rounded"
                        value={formData.phone}
                        onChange={(e) => setFormData({ ...formData, phone: e.target.value })}
                    />
                </div>
            )}

            {/* B∆∞·ªõc 3: X√°c nh·∫≠n */}
            {step === 3 && (
                <div className="bg-white p-6 rounded-lg shadow w-full max-w-md mb-6 text-left">
                    <p><strong>H·ªç t√™n:</strong> {formData.name}</p>
                    <p><strong>Email:</strong> {formData.email}</p>
                    <p><strong>S·ªë ƒëi·ªán tho·∫°i:</strong> {formData.phone}</p>
                    <p><strong>Gh·∫ø ƒë√£ ch·ªçn:</strong> {selectedSeats.join(', ')}</p>
                </div>
            )}

            {/* N√∫t ƒëi·ªÅu h∆∞·ªõng */}
            <div className="flex gap-4">
                {step > 1 && (
                    <button
                        className="px-6 py-3 bg-gray-500 text-white rounded-lg hover:bg-gray-600"
                        onClick={handleBack}
                    >
                        ‚Üê Quay l·∫°i
                    </button>
                )}
                {step < 3 && (
                    <button
                        className="px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700"
                        onClick={handleNext}
                    >
                        Ti·∫øp t·ª•c ‚Üí
                    </button>
                )}
                {step === 3 && (
                    <button
                        className="px-6 py-3 bg-green-600 text-white rounded-lg hover:bg-green-700"
                        onClick={handleConfirm}
                    >
                        X√°c nh·∫≠n ƒë·∫∑t v√©
                    </button>
                )}
            </div>
        </div>
    );
};

export default SeatSelection;
